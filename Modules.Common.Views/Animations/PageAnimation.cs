namespace Modules.Common.Views.Animations;

/// <summary>
/// Styles of page animations for appearing/disappearing
/// </summary>
public enum PageAnimation
{
    /// <summary>
    /// No animation takes place
    /// </summary>
    None,

    /// <summary>
    /// The page fades in
    /// </summary>
    FadeIn,

    /// <summary>
    /// The page fades out
    /// </summary>
    FadeOut,
}