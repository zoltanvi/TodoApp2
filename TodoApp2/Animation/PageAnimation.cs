namespace TodoApp2
{
    /// <summary>
    /// Styles of page animations for appearing/disappearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,

        /// <summary>
        /// The page fades in
        /// </summary>
        FadeIn = 1,
        
        /// <summary>
        /// The page fades out
        /// </summary>
        FadeOut = 2
    }
}
